package MyLessons;

public class ForLoop {
    public static void main(String[] args) throws Exception {
    for(int i = 0; i <= 10; i++){
        for(int j = 1; j <= 10; j++){
            if(i == 0) {
                System.out.print(8);
            }else{
                System.out.print(8);
                break;
            }
        }
        System.out.println();
    }

}
}
